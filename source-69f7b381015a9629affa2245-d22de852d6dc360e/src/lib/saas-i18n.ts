export type SaasTranslations = typeof fr

/**
 * Retourne les traductions SaaS dans la locale demandée.
 * Usage : `const t = tSaas(locale); t.portal.dashboard.title`
 */
export function tSaas(locale: Locale): SaasTranslations {
  return locale === 'en' ? en : fr
}
